#!/usr/bin/env node
// Reads contracts/deployed-addresses.json and writes .env.local
// Run after: cd contracts && npx hardhat run scripts/deploy.ts --network localhost

const fs   = require('fs');
const path = require('path');

const ADDRS_FILE = path.join(__dirname, '..', 'contracts', 'deployed-addresses.json');
const ENV_FILE   = path.join(__dirname, '..', '.env.local');

if (!fs.existsSync(ADDRS_FILE)) {
  console.error('✗  contracts/deployed-addresses.json not found.');
  console.error('   Run: cd contracts && npx hardhat run scripts/deploy.ts --network localhost');
  process.exit(1);
}

const d = JSON.parse(fs.readFileSync(ADDRS_FILE, 'utf8'));
const c = d.contracts;
const n = d.native;

const env = `# Auto-generated by scripts/sync-addresses.js
NEXT_PUBLIC_LOCAL_VAULT=${c.ByNdVault}
NEXT_PUBLIC_LOCAL_STAKING=${c.ByNdStaking}
NEXT_PUBLIC_LOCAL_VOTER=${c.ByNdVoter}
NEXT_PUBLIC_LOCAL_VEBYND=${c.VeBYND}
NEXT_PUBLIC_LOCAL_VEMEZO=${n.VeMEZO}
NEXT_PUBLIC_LOCAL_MUSD=${n.MUSD}
`;

fs.writeFileSync(ENV_FILE, env);
console.log('✓  .env.local written:');
console.log('   ByNdVault  ', c.ByNdVault);
console.log('   ByNdStaking', c.ByNdStaking);
console.log('   ByNdVoter  ', c.ByNdVoter);
console.log('   VeBYND     ', c.VeBYND);
console.log('   MockVeMEZO ', n.VeMEZO);
console.log('');
console.log('✓  Restart npm run dev to pick up new addresses');